    private void txtSCHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSCHDActionPerformed
        
        if(txtSCHD.getText().trim().length()>0){
            try {
                Schedule OBJ = new ScheduleController().getSchedule(txtSCHD.getText().trim());
                if(OBJ!=null){
                    
                    txtSCHID.setText(OBJ.getSCHID());
                    txtDate.setText(OBJ.getSHDate());
                    txtSHStart.setText(OBJ.getSHStart());
                    txtEnd.setText(OBJ.getSHEnd());
                }else{
                    JOptionPane.showMessageDialog(this, "No results found", "Warning", JOptionPane.WARNING_MESSAGE);
                }   } catch (SQLException ex) {
                Logger.getLogger(ScheduleManagerView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ScheduleManagerView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
        JOptionPane.showMessageDialog(this, "Please Insert Record Number", "Error", JOptionPane.ERROR_MESSAGE);
        
        }    
    }//GEN-LAST:event_txtSCHDActionPerformed

