    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        try {
            String SCHD = null;
            String SCHID = txtSCHID.getText();
            String SHDate = txtDate.getText();
            String SHStart = txtSHStart.getText();
            String SHEnd = txtEnd.getText();
            
            Schedule s1 = new Schedule(
                    SCHD,
                    SCHID,
                    SHDate,
                    SHStart,
                    SHEnd
            );
            
            boolean isSaved = new ScheduleController().saveSchedule(s1);
            
            if(isSaved){
            JOptionPane.showMessageDialog(null, "Saved");
            }else{
            JOptionPane.showMessageDialog(null, "Try again");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ScheduleManagerView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ScheduleManagerView.class.getName()).log(Level.SEVERE, null, ex);
        }
               
    }//GEN-LAST:event_btnSaveActionPerformed

