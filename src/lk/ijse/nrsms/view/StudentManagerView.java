    private void txtSIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSIDActionPerformed
        if(txtSID.getText().trim().length()>0){
            try {
                Student OBJ = new StudentController().getStudent(txtSID.getText().trim());
                
                if(OBJ!=null){
                    txtName.setText(OBJ.getName());
                    txtAddress.setText(OBJ.getAddress());
                    txtSchool.setText(OBJ.getSchool());
                    cmbTClass.setSelectedItem(OBJ.getClass());
                    txtTeleM.setText(OBJ.getTeleM());
                    txtTeleH.setText(OBJ.getTeleH());
                    
                }else{
                    JOptionPane.showMessageDialog(this, "No Results Found","Warning",JOptionPane.WARNING_MESSAGE); 
                }
            } catch (SQLException ex) {
                Logger.getLogger(StudentManagerView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(StudentManagerView.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
        JOptionPane.showMessageDialog(this, "Please Insert an SID","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSIDActionPerformed

