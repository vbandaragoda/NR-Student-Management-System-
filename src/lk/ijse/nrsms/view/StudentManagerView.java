    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        if(txtSID.getText().trim().length()>4){
            
            try {
                String SID = txtSID.getText();
                String Name = txtName.getText();
                String Address = txtAddress.getText();
                String School = txtSchool.getText();
                String TClass = cmbTClass.getSelectedItem().toString();
                String TeleM = txtTeleM.getText();
                String TeleH = txtTeleH.getText();
                
                Student s1 = new Student(SID,Name,Address,School,TClass,TeleM,TeleH);
                
                if(new StudentController().updateState(s1)){
                    JOptionPane.showMessageDialog(null, "Done...");
                }else{
                    JOptionPane.showMessageDialog(null, "Please try again", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(StudentManagerView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(StudentManagerView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
        JOptionPane.showMessageDialog(null, "Please Insert Correct ID", "Error", JOptionPane.ERROR_MESSAGE);
        
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

